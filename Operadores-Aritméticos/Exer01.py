# Exercício 1: Calculadora Simples

# Objetivo: Pedir dois números para o usuário e mostrar o resultado das 4 operações básicas entre eles.

num1 = int(input('Digite o primeiro número: '))
num2 = int(input('Digite o segundo número: '))

print(f'A soma é: {float(num1 + num2)}')
print(f'A subtração é: {float(num1 - num2)}')
print(f'A multiplicação é: {float(num1 * num2)}')
print(f'A divisão é: {float(num1 / num2)}')
